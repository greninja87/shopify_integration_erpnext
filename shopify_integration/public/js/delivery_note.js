frappe.ui.form.on('Delivery Note', {
    refresh: function(frm) {
        if (!frm.doc.items || frm.doc.items.length === 0) return;

        // Check if any item is linked to a Sales Order
        let so_name = null;
        for (let item of frm.doc.items) {
            if (item.against_sales_order) {
                so_name = item.against_sales_order;
                break;
            }
        }

        if (!so_name) return;

        // Single server call to check all conditions at once:
        // 1. Is this SO from Shopify?
        // 2. Is auto-invoice after DN enabled for this store?
        // 3. Does a Sales Invoice already exist for this DN?
        frappe.xcall(
            'frappe.client.get_value',
            {
                doctype: 'Sales Order',
                filters: { name: so_name },
                fieldname: ['shopify_order_id', 'shopify_store']
            }
        ).then(function(so_data) {
            if (!so_data || !so_data.shopify_order_id) return;

            let store = so_data.shopify_store;

            return frappe.xcall(
                'frappe.client.get_value',
                {
                    doctype: 'Shopify Settings',
                    filters: { shop_domain: store, enable_sales_invoice: 1, sales_invoice_trigger: 'After Delivery Note' },
                    fieldname: ['name']
                }
            ).then(function(settings_data) {
                if (!settings_data || !settings_data.name) return;

                return frappe.xcall(
                    'frappe.client.get_count',
                    {
                        doctype: 'Sales Invoice Item',
                        filters: {
                            delivery_note: frm.doc.name,
                            docstatus: ['!=', 2]
                        }
                    }
                ).then(function(si_count) {
                    if (si_count && si_count > 0) {
                        // SI already exists — don't show banner
                        return;
                    }

                    // No SI yet — show the banner
                    frm.dashboard.clear_headline();
                    frm.dashboard.set_headline_alert(
                        __('<div style="padding-right: 40px; display: block; line-height: 1.5;"><b>Shopify Order:</b> The Sales Invoice for this Delivery Note will be automatically generated by the Shopify Integration scheduler. You do not need to create it manually.</div>'),
                        'yellow'
                    );

                    // Hide the "Sales Invoice" option from the Create button
                    setTimeout(() => {
                        if (frm.custom_buttons && frm.custom_buttons['Sales Invoice']) {
                            frm.remove_custom_button('Sales Invoice', 'Create');
                        }
                    }, 100);
                });
            });
        });
    }
});
