frappe.ui.form.on('Delivery Note', {
    refresh: function(frm) {
        // Single server call handles all checks:
        //   1. Is this DN linked to a Shopify SO?
        //   2. Is auto-SI-after-DN enabled for that store?
        //   3. Does a Sales Invoice already exist for this DN?
        // Using a whitelisted Python method avoids querying Sales Invoice Item
        // (a child DocType) directly through frappe.client, which throws
        // "Sales Invoice Item is not a valid parent DocType" for any user.
        frappe.xcall(
            'shopify_integration.utils.sales_invoice.get_dn_shopify_invoice_status',
            { dn_name: frm.doc.name }
        ).then(function(info) {
            if (!info || !info.is_shopify || info.has_si) return;

            let msg;
            if (info.si_timing === 'Immediate') {
                msg = __('<b>Shopify Order:</b> The Sales Invoice for this Delivery Note will be created automatically upon submission. You do not need to create it manually.');
            } else if (info.delay_hours > 0) {
                msg = __('<b>Shopify Order:</b> The Sales Invoice will be automatically generated by the Shopify Integration scheduler {0} hour(s) after this Delivery Note is submitted. You do not need to create it manually.', [info.delay_hours]);
            } else {
                msg = __('<b>Shopify Order:</b> The Sales Invoice for this Delivery Note will be automatically generated by the Shopify Integration scheduler. You do not need to create it manually.');
            }

            frm.dashboard.clear_headline();
            frm.dashboard.set_headline_alert(
                '<div style="padding-right:40px;display:block;line-height:1.5;">' + msg + '</div>',
                'yellow'
            );

            // Hide the "Sales Invoice" option from the Create button
            setTimeout(() => {
                if (frm.custom_buttons && frm.custom_buttons['Sales Invoice']) {
                    frm.remove_custom_button('Sales Invoice', 'Create');
                }
            }, 100);
        });
    }
});
